def get_zip_centroid(zip_code: str) -> Optional[dict]:
    """
    Returns centroid data for a US ZIP code.

    Args:
        zip_code: 5-digit US ZIP code string.

    Returns:
        Dict with keys 'lat', 'lon', 'city', 'state', or None if not found.
    """
    zip_code = str(zip_code).strip().zfill(5)
    entry = _ZIP_DATA.get(zip_code)
    if entry is None:
        return None
    lat, lon, city, state = entry
    return {"lat": lat, "lon": lon, "city": city, "state": state}
